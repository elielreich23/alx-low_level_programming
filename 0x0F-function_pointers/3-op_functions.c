#include "3-calc.h"

/**
*op_add - Returns the sum of two numbers
*@a:the first number
*@b:the second number
*
*Return:The sum of a and b
*/

int op_add(int a, int b)
{
return (a + b);
}

/**
*op_sub - Returns the substraction of two int
*@a:the first number
*@b:the second number
*
*Return:The diffrence of a and b
*/

int op_sub(int a, int b)
{
return (a - b);
}

/**
*op_mul - Returns the product of two int
*@a:the first number
*@b:the second number
*
*Return:The product of a and b
*/

int op_mul(int a, int b)
{
return (a * b);
}

/**
*op_div - Returns the division of two int
*@a:the first number
*@b:the second number
*
*Return:The division of a and b
*/

int op_div(int a, int b)
{
return (a / b);
}

/**
*op_mod - Returns the remainder of the division of two int
*@a:the first number
*@b:the second number
*
*Return:The remainder of the division of a and b
*/

int op_mod(int a, int b)
{
return (a % b);
}
